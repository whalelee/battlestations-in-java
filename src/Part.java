public class Part extends WeaponPart{

	private int speed;
	private int hp;
	private int capacity;


	public int getHP(){
		return hp;
	}
	public void setHP(int hp){
		this.hp = hp;
	}

	public int getCapacity(){
		return capacity;
	}
	public void setCapacity(int capacity){
		this.capacity = capacity;
	}

	public int getSpeed(){
		return speed;
	}
	public void setSpeed(int speed){
		this.speed = speed;
	}

}